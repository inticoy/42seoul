int	ft_ispercent(int c)
{
	if (c == '%')
		return (1);
	else
		return (0);
}

int	ft_isflag(int c)
{
	if (c == '-' || c == '+' || c == ' ' || c == '#' || c == '0')
		return (1);
	else
		return (0);
}

int	ft_isdot(int c)
{
	if (c == '.')
		return (1);
	else
		return (0);
}
